import random

# randit function use when we want to create random integer number
random_int = random.randint(1 ,9)
print(random_int)

# .random function use when we want to create decimal values 0 to 1
random_number_0to_1 = random.random()
print(random_number_0to_1)

# random.uniform use to create random number by range float number
random_uniform = random.uniform(1,99)
print(random_uniform)